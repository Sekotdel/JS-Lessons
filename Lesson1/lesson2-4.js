/** Реализовать основные 4 арифметические операции (+, -, /, *) в виде функций с 
двумя параметрами. Т.е. например, функция для сложения должна принимать два числа,
складывать их и возвращать результат. Обязательно использовать оператор return. */

function summ(a, b){
    return a + b;
}
    console.log (summ(1, 2));

function diff(a, b){
    return a - b;
}
    console.log(diff(3, 4));

function multi(a, b) {
    return a * b;    
}
    console.log(multi(5, 6));

function split(a, b) {
    return a / b;
}
    console.log(split(7, 8));